return {
  'nvim-treesitter/nvim-treesitter',
  dependencies = {
    'nvim-treesitter/nvim-treesitter-textobjects',
  },
  build = ':TSUpdate',
  opts = {
    modules = {},
    sync_install = false,
    ignore_install = {},

    ensure_installed = { 'c', 'cpp', 'java', 'lua', 'python',  'tsx', 'typescript', 'vimdoc', 'vim', 'arduino' },

    auto_install = false,

    highlight = {
      enable = true,
      disable = {'dart'},
    },
    indent = {
      enable = true,
      disable = {'dart'},
    },
    incremental_selection = {
      enable = true,
      disable = {'dart'},
      keymaps = {
        init_selection = '<c-space>',
        node_incremental = '<c-space>',
        scope_incremental = '<c-s>',
        node_decremental = '<M-space>',
      },
    },
    textobjects = {
      select = {
        enable = true,
        disable = {'dart'},
        lookahead = true, -- Automatically jump forward to textobj, similar to targets.vim
        keymaps = {
          -- You can use the capture groups defined in textobjects.scm
          ['aa'] = '@parameter.outer',
          ['ia'] = '@parameter.inner',
          ['af'] = '@function.outer',
          ['if'] = '@function.inner',
          ['ac'] = '@class.outer',
          ['ic'] = '@class.inner',
